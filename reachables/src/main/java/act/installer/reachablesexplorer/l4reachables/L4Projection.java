public class L4Projection {

  private List<String> substrates;
  private String ros;
  private List<String> products;

  public L4Projection() {}

  public List<String> getSubstrates() {
    return substrates;
  }

  public String getRos() {
    return ros;
  }

  public List<String> getProducts() {
    return products;
  }
}
